package br.com.adeusunibratec.bean;

public class PortaModuloGson {
	public String Identificador;
	public String NumeroPorta;
	public int TipoPorta;
	public int Modulo;

	public String getIdentificador() {
		return Identificador;
	}

	public void setIdentificador(String identificador) {
		Identificador = identificador;
	}

	public String getNumeroPorta() {
		return NumeroPorta;
	}

	public void setNumeroPorta(String numeroPorta) {
		NumeroPorta = numeroPorta;
	}

	public int getTipoPorta() {
		return TipoPorta;
	}

	public void setTipoPorta(int tipoPorta) {
		TipoPorta = tipoPorta;
	}

	public int getModulo() {
		return Modulo;
	}

	public void setModulo(int modulo) {
		Modulo = modulo;
	}
}
